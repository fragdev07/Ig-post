let isLess = false;
let toggleBtn = document.querySelector("#toggleBtn");
let caption = document.querySelector("#post-info p");

toggleBtn.addEventListener("click", () => {
  if (!isLess) {
    toggleBtn.textContent = "less";
    caption.classList.remove("line-clamp-1");
  } else {
    toggleBtn.textContent = "more";
    caption.classList.add("line-clamp-1");
  }
  console.log('working');
  
  isLess = !isLess; // Don't forget this!
});


